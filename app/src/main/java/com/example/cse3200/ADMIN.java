package com.example.cse3200;

public class ADMIN {
    String email;
    String id;

    public ADMIN() {
    }

    public ADMIN(String id, String email) {
        this.id = id;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
